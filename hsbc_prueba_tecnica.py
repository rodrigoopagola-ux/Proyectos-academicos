"""## Random forests (using capital gains)

#Assumptions Made
1. **Missing at Random:** It was assumed that missing values in age and hours worked were random, imputing them with the spatial median.
2. **Financial Zeros:** It was assumed that missing data in `capital_gain` and `capital_loss` equals a value of $0.
3. **Sample Independence:** It is assumed that each row represents an independent individual and that the training subset (80%) is a faithful representation of the total universe.
4. **Cost of Error:** It is assumed that the business cost of a False Negative (ignoring a >50K client) is greater than that of a False Positive, justifying the use of balanced class weights.
"""

# Random forests columns

cols_finales = [
    'age_of_client',
    'hours_of_work_per_week',
    'education_level',
    'occupation',
    'type_of_relationship',
    'capital_gain',
    'capital_loss'
]

X = df_data_explor[cols_finales].copy()
y = df_data_explor['earnings_flag_id']

# Imputing nulls
X['age_of_client'] = X['age_of_client'].fillna(X['age_of_client'].median())
X['hours_of_work_per_week'] = X['hours_of_work_per_week'].fillna(X['hours_of_work_per_week'].median())
X['capital_gain'] = X['capital_gain'].fillna(0)
X['capital_loss'] = X['capital_loss'].fillna(0)

# One-Hot Encoding
X_encoded = pd.get_dummies(X, drop_first=True)

# Stratified Split (80/20)
X_train, X_test, y_train, y_test = train_test_split(
    X_encoded, y, test_size=0.2, random_state=42, stratify=y
)


# MODEL TRAINING (RANDOM FOREST)

print("Training Optimized Random Forest...")

modelo_rf_final = RandomForestClassifier(
    n_estimators=200,
    class_weight='balanced',
    max_depth=15,
    n_jobs=-1,
    random_state=42
)

modelo_rf_final.fit(X_train, y_train)


# PREDICTIONS, METRICS & UNCERTAINTY

y_pred = modelo_rf_final.predict(X_test)
y_prob = modelo_rf_final.predict_proba(X_test)[:, 1]

# Extracting individual tree probabilities
predicciones_individuales = np.array([arbol.predict_proba(X_test.values)[:, 1] for arbol in modelo_rf_final.estimators_])

prob_inferior = np.percentile(predicciones_individuales, 5, axis=0)
prob_superior = np.percentile(predicciones_individuales, 95, axis=0)

