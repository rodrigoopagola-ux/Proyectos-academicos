"""## Methodology Selection
Several classification methodologies were evaluated:
1. **Logistic Regression:** (Pros: High interpretability. Cons: Fails with non-linear relationships and multicollinearity).
2. **Discriminant Analysis (LDA/QDA):** (Pros: Uses Mahalanobis distances. Cons: Collapses with sparse matrices from One-Hot Encoding).
3. **Support Vector Machines (SVM):** (Pros: Robust in high dimensionality. Cons: Prohibitive computational cost for large samples).
4. **Gradient Boosting (XGBoost):** (Pros: Maximum predictive performance. Cons: Higher tendency to overfit if not tuned correctly).
5. **Random Forest:** (Pros: Immune to scale, handles skewed variables. Cons: acts like a blackbox and during training can learn the dataset perfectly, skewing real life data in production).

**Final Selection:** **Random Forest** was chosen. This methodology allows us to isolate the noise from transformed categorical variables, process the skewness of financial variables without standardization, and solve the computational bottleneck by parallelizing the creation of multiple decision trees.
"""

# Logistical regression

